function extractSnippets(allSnippets) {
  const snippetList = allSnippets.split("==========\n");
  return snippetList
    .map((snippet) => snippet.split("\n"))
    .reduce((acc, [title, locationAndDate = "", _, snippet]) => {
      if (!title) return acc;
      const [location, date] = locationAndDate.split(" | ");
      const existingSnippets = acc.get(title) || [];
      // console.log("zzz", { title, locationAndDate, _ });
      return acc.set(title, [
        ...existingSnippets,
        {
          location: location
            .replace("- Your Highlight on page ", "")
            .replace("- Your Highlight at location ", ""),
          date: date.replace(/Added on (\w*), /, ""),
          snippet,
        },
      ]);
    }, new Map());
}

function main() {
  const groupedSnippets = extractSnippets(snippets);
  Array.from(groupedSnippets.entries()).forEach(([title, snippets]) => {
    const fileName = title.toLowerCase().trim().split(" ").join("-");
    const lineBreak = `\n_________________\n`;
    const snippetContent = snippets
      .map(({ location, snippet }) => `Page: ${location}  \n> ${snippet}`)
      .join(lineBreak);
    fs.writeFileSync(
      path.join(process.cwd(), "content/snippets", `${fileName}.md`),
      snippetContent
    );
  });
}

